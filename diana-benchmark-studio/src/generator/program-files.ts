async function hashBytes(bytes: Uint8Array): Promise<string> {
  const digest = await crypto.subtle.digest("SHA-256", bytes as BufferSource);
  return Array.from(new Uint8Array(digest), (byte) => byte.toString(16).padStart(2, "0")).join("");
}

async function compileProgramFiles(input: unknown, allowScientificBlockers: boolean): Promise<{ spec: ReturnType<typeof normalizeTaskSpec>; fingerprint: string; files: ProgramFileMap; manifest: ProgramManifest }> {
  const spec = normalizeTaskSpec(input); const fingerprint = await fingerprintTaskSpec(spec); const notebook = allowScientificBlockers ? await notebookPreviewBytes(spec) : await notebookBytes(spec);
  const readme = `# ${spec.project.name}\n\nGenerated by Diana Benchmark Studio. Fingerprint: \`${fingerprint}\`.\n\n## Run\n\n1. Create a Python 3.11 environment.\n2. Install: \`python -m pip install -r requirements.txt\`.\n3. Run Windows: \`powershell -ExecutionPolicy Bypass -File RUN_WINDOWS.ps1\`.\n4. Run shell: \`./run.sh\`.\n\nThe default custom fixture is **SYNTHETIC EXAMPLE — NOT HEALTH DATA**. For local governed data, set execution mode in the configuration and supply the fixed relative input path. Hormonbench users must obtain mcPHASES directly under its DUA and set the documented environment variables.\n`;
  const runHeadless = `from pathlib import Path\nimport nbformat\nfrom nbclient import NotebookClient\npath = Path(__file__).with_name("benchmark.ipynb")\nnb = nbformat.read(path, as_version=4)\nNotebookClient(nb, timeout=180, kernel_name="python3", resources={"metadata": {"path": str(path.parent)}}, store_widget_state=False).execute()\nprint("Benchmark Notebook completed")\n`;
  const files: ProgramFileMap = {
    [`${ROOT}README.md`]: encode(readme),
    [`${ROOT}TASK_CARD.md`]: encode(generateTaskCard(spec, fingerprint)),
    [`${ROOT}DATA_CARD_TEMPLATE.md`]: encode(generateDataCard(spec)),
    [`${ROOT}NOTICE.md`]: encode("# Notice\n\nThis kit contains no governed dataset. Research outputs are nonclinical. Third-party datasets and packages retain their own licences.\n"),
    [`${ROOT}LICENSE`]: encode(LICENSE),
    [`${ROOT}benchmark.ipynb`]: notebook,
    [`${ROOT}benchmark_config.json`]: encode(`${JSON.stringify(spec, null, 2)}\n`),
    [`${ROOT}benchmark_config.schema.json`]: encode(configJsonSchema()),
    [`${ROOT}requirements.txt`]: encode(generateRequirements(spec)),
    [`${ROOT}RUN_WINDOWS.ps1`]: encode("$ErrorActionPreference = 'Stop'\npython -m pip install -r requirements.txt\npython run_headless.py\n"),
    [`${ROOT}run.sh`]: encode("#!/usr/bin/env sh\nset -eu\npython -m pip install -r requirements.txt\npython run_headless.py\n"),
    [`${ROOT}run_headless.py`]: encode(runHeadless),
    [`${ROOT}schemas/prediction.schema.json`]: encode(predictionSchema(spec)),
    [`${ROOT}schemas/public_result.schema.json`]: encode(publicResultSchema(spec)),
    [`${ROOT}data/README.md`]: encode("# Local data\n\nReplace the synthetic example only on your local governed system. Never commit licensed or participant-level rows.\n"),
    [`${ROOT}data/example_synthetic.csv`]: encode(generateSyntheticCsv(spec)),
    [`${ROOT}results/README.md`]: encode("# Results\n\nThe Notebook writes aggregate metrics, leaderboard, and run manifest here. Treat optional row predictions as private.\n"),
  };
  const hashes: Record<string, string> = {};
  for (const path of Object.keys(files).sort()) hashes[path.slice(ROOT.length)] = await hashBytes(files[path]);
  const manifest: ProgramManifest = {
    schema_version: 1, generator: { name: "Diana Benchmark Studio", version: "1.0.0", template_version: "2026-07-19" },
    mode: spec.mode, validation_level: spec.validation_level, kit_id: `diana-${fingerprint.slice(0, 16)}`,
    task_config_sha256: await sha256Text(canonicalize(spec)), notebook_sha256: hashes["benchmark.ipynb"],
    task_identity: { name: spec.project.name, task_id: spec.official?.task_id ?? spec.project.task_id, version: spec.official?.task_version ?? "draft" },
    selected_components: { models: spec.official ? [...spec.official.baseline_ids] : spec.models.map((model) => model.id), metrics: [spec.metrics.primary, ...spec.metrics.secondary], split: spec.split.type },
    files: hashes, expected_outputs: [`${spec.output.directory}/metrics.json`, `${spec.output.directory}/leaderboard.csv`, `${spec.output.directory}/run_manifest.json`],
    source_provenance: spec.official ? { commit: spec.official.source_commit, task_spec_hash: spec.official.task_spec_hash, fold_hash: spec.official.fold_hash, input_schema_hash: spec.official.input_schema_hash, runner: spec.official.runner_path } : undefined,
  };
  files[`${ROOT}manifest.json`] = encode(`${JSON.stringify(manifest, null, 2)}\n`);
  return { spec, fingerprint, files, manifest };
}

export function generateProgramFiles(input: unknown) { return compileProgramFiles(input, false); }
export function previewProgramFiles(input: unknown) { return compileProgramFiles(input, true); }

export { ROOT as KIT_ROOT, hashBytes };
